package LAB_01.Q01;

public class Calculator {
    public static int sum(int a,int b){
        return (a+b);
    }
    public static int diff(int a,int b){
        return (a-b);
    }
    public static int mul(int a,int b){
        return (a*b);
    }
    public static int div(int a,int b){
        return (a/b);
    }
    public static int rem(int a,int b){
        return (a%b);
    }
}
